#include "s21_string.h"  // Подключаем заголовочный файл, содержащий объявления функций и типов для работы со строками и памятью.
// Копирует символ c (беззнаковый тип) в первые n символов строки, на которую
// указывает аргумент str.

// char str[] = "Пример заполнения памяти";
